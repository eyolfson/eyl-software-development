void i8hex_write(int fd, uint8_t *data, size_t size)
{
	uint8_t *current = data;
	size_t remaining = size;

	uint16_t address = 0x0000;

	char buf[46];

	while (remaining >= 16) {
		uint8_t checksum = 0;
		checksum += 16;                        // Length
		checksum += ((address & 0xFF00) >> 8); // Address (high)
		checksum += (address & 0xFF);          // Address (low)
		checksum += 0;                         // Record type
		for (int i = 0; i < 16; ++i) {
			checksum += current[i];
		}
		checksum = ~checksum + 1;

		snprintf(buf, ARRAY_SIZE(buf),
		         ":10%04X00%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X"
		         "%02X%02X%02X%02X%02X%02X%02X\r\n", address,
		         current[0], current[1], current[2], current[3],
		         current[4], current[5], current[6], current[7],
		         current[8], current[9], current[10], current[11],
		         current[12], current[13], current[14], current[15],
		         checksum);
		write(fd, buf, ARRAY_SIZE(buf) - 1);

		current += 16;
		remaining -= 16;
		address += 16;
	}

	if (remaining > 0) {
		uint8_t checksum = 0;
		checksum += remaining;                 // Length
		checksum += ((address & 0xFF00) >> 8); // Address (high)
		checksum += (address & 0xFF);          // Address (low)
		checksum += 0;                         // Record type
		for (size_t i = 0; i < remaining; ++i) {
			checksum += current[i];
		}
		checksum = ~checksum + 1;

		snprintf(buf, ARRAY_SIZE(buf),
		         ":%02X%04X00", remaining, address);
		write(fd, buf, 9);
		for (size_t i = 0; i < remaining; ++i) {
			snprintf(buf, ARRAY_SIZE(buf), "%02X", current[i]);
			write(fd, buf, 2);
		}
		snprintf(buf, ARRAY_SIZE(buf), "%02X\r\n", checksum);
		write(fd, buf, 4);
	}

	snprintf(buf, ARRAY_SIZE(buf), ":00000001FF\r\n");
	write(fd, buf, 13);
}

int main(int argc, const char *const *argv)
{
	if (argc != 3) {
		printf("%s <input binary> <output hex>\n", argv[0]);
		return 1;
	}

	int input_fd = open(argv[1], O_RDONLY);
	if (input_fd == -1) {
		return 1;
	}

	struct stat stat;
	if (fstat(input_fd, &stat) == -1) {
		return 1;
	}

	ssize_t binary_size = stat.st_size;
	uint8_t *binary_data = malloc(binary_size);
	if (binary_data == NULL) {
		return 1;
	}

	if (read(input_fd, binary_data, binary_size) != binary_size) {
		return 1;
	}

	if (close(input_fd) == -1) {
		return 1;
	}

	mode_t mode = S_IRUSR | S_IWUSR | S_IRGRP | S_IROTH;
	int fd = open(argv[2], O_RDWR | O_CREAT, mode);
	if (fd == -1) {
		return 1;
	}

	ftruncate(fd, 0);
	i8hex_write(fd, binary_data, binary_size);

	free(binary_data);

	if (close(fd) == -1) {
		return 1;
	}

	return 0;
}
